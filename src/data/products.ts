export const getCategoryColor = (category: string) => {
  const colors: Record<string, string> = {
    Laptops: 'bg-blue-500/20 text-blue-400',
    Tablets: 'bg-purple-500/20 text-purple-400',
    Phones: 'bg-pink-500/20 text-pink-400',
    Audio: 'bg-green-500/20 text-green-400',
    Accessories: 'bg-gray-500/20 text-gray-400',
    Wearables: 'bg-orange-500/20 text-orange-400',
    Displays: 'bg-cyan-500/20 text-cyan-400',
    Desktops: 'bg-indigo-500/20 text-indigo-400',
    Entertainment: 'bg-red-500/20 text-red-400',
  };
  return colors[category] || 'bg-gray-500/20 text-gray-400';
};

export const getStatusColor = (status: Product['status']) => {
  const colors = {
    active: 'bg-emerald-500/20 text-emerald-400',
    draft: 'bg-yellow-500/20 text-yellow-400',
    archived: 'bg-red-500/20 text-red-400',
  };
  return colors[status];
};

export const getStockStatus = (stock: number) => {
  if (stock === 0) return { label: 'Out of Stock', color: 'text-red-400' };
  if (stock < 20) return { label: 'Low Stock', color: 'text-yellow-400' };
  if (stock < 50) return { label: 'Medium Stock', color: 'text-blue-400' };
  return { label: 'In Stock', color: 'text-emerald-400' };
};
